    private void btnImagemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImagemActionPerformed
        // TODO add your handling code here:
        FileNameExtensionFilter imageFilter = 
                new FileNameExtensionFilter("Image files", ImageIO.getReaderFileSuffixes());
        JFileChooser jFileChooser2 = new JFileChooser();
        jFileChooser2.addChoosableFileFilter(imageFilter);
        if (jFileChooser2.showOpenDialog(this) ==  JFileChooser.APPROVE_OPTION){
            imagem = new Imagem(jFileChooser2.getSelectedFile());
            jLabelImagem.setIcon(imagem.getImageIcon());
        }
    }//GEN-LAST:event_btnImagemActionPerformed

