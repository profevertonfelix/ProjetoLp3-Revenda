    private void btnEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarActionPerformed
        // TODO add your handling code here:
        boolean result = false;
        int codCategoria = ComboBoxUsuario.getSelectedIndex(jComboBoxCategoria);        

        if (veiculo != null) {
            // editando
            Veiculo veiculoEditar;
            veiculoEditar = 
                    new Veiculo(veiculo.getPlaca(), inputModelo.getText(), inputCor.getText(), new Categoria(codCategoria), imagem.getImagem());

            result = 
                    RevendaCliente.conexaocontroller.
                            veiculoEditar(veiculoEditar);
        } else {
            if (inputPlaca.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Preencha o Nome!");
                inputPlaca.requestFocus();
            } else {
                Veiculo veiculoInserir = 
                        new Veiculo(inputPlaca.getText(), inputModelo.getText(), inputCor.getText(), new Categoria(codCategoria), imagem.getImagem());


                result = RevendaCliente.conexaocontroller.veiculoInserir(veiculoInserir);
            }
            
        }
        if (result) {
                JOptionPane.showMessageDialog(this, "Veiculo adicionado!");
                dispose();
                
            } else {
                JOptionPane.showMessageDialog(this, "Erro adicionando/editando Veiculo!");
            }

    }//GEN-LAST:event_btnEnviarActionPerformed

